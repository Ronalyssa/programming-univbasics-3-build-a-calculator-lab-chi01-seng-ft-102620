def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  difference = num1 - num2
  puts difference
end

def division(num1, num2)
  quotient = num1 / num2
  puts quotient
end

def multiplication(num1, num2)
  multiply = num1 * num2
  puts multiply
end

def modulo(num1, num2)
  remainder = num1 % num2
  puts remainder
end

def square_root(num)
  root = Math.sqrt(num)
  puts root
end
